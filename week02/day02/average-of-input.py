# Write a program that asks for 5 integers in a row,
# then it should print the sum and the average of these numbers like:
#
# Sum: 22, Average: 4.4

print ("Add 5 numbers in a row:")
int1 = int(input())
int2 = int(input())
int3 = int(input())
int4 = int(input())
int5 = int(input())


print ("Sum: ", int1+int2+int3+int4+int5)
print ("Average: ", (int1+int2+int3+int4+int5)/5)
